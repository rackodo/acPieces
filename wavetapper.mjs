class Instrument {
	constructor(active, [onR, onG, onB], [offR, offG, offB], ...shapes) {
		this.active = (active ? active : false)
		this.on = [onR, onG, onB]
		this.off = [offR, offG, offB]
		this.shapes = shapes
	}

	drawShape(offsetX, offsetY, ink, shapeIndex) {
		if (this.active) {
			ink(this.off).box(offsetX - 1, offsetY - 1, instrumentSize, instrumentSize)

			this.shapes[shapeIndex].shape.forEach(
				(row, rowI) => {
					row.forEach(
						(col, colI) => {
							if (col == 1) {
								ink(this.on).box(offsetX + colI - 1, offsetY + rowI - 1, 1, 1)
							}
					})
			})
		}
	}
}

